package pattern;

public class LH_pattern {

	public static void main(String[] args) {
		System.out.println("hi bro");
		int i,j,k;
		for(i=1;i<=4;i++)
		{
		    for(j=i;j<4;j++)
		    {
		    System.out.print("   ");
		    }
		    for(k=1;k<=i;k++)
		    {
		    System.out.print(" * ");
		    }
		    System.out.println();
		}
		System.out.println("how are you bro");

	}

}
